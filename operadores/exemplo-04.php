<?php 

//Declarando variáveis
$a = 55.1;

$b = 55;

//sinal de maior
var_dump($a > $b);

echo "<br>";

//sinal de menor
var_dump($a < $b);

echo "<br>";

//compara o tipo e valor
var_dump($a === $b);

echo "<br>";

//compara o valor
var_dump($a == $b);

echo "<br>";

//diferente ou igual
var_dump($a != $b);

echo "<br>";

//não é igual ou não são do mesmo tipo
var_dump($a !== $b);


?>